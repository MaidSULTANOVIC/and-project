package com.example.gamelife.leagueoflegends;

public class LolStat {
    private boolean win;
    private int kills;
    private int deaths;
    private int assists;


    public boolean isWin() {
        return win;
    }

    public int getKills() {
        return kills;
    }

    public int getDeaths() {
        return deaths;
    }

    public int getAssists() {
        return assists;
    }
}
